 *          SetFloat        设置 Float 的键值对
 *          GetFloat        获取某一键对应的 Float 值
 *          SetInt          设置 Int 的键值对
 *          GetInt          获取某一键对应的 Int 值
 *          SetString       设置 String 的键值对   
 *          GetString       获取某一键对应的 String 值
 *
 *          HasKey          判断是否存在某一个key值
 *          DeleteKey       删除键值对
 *          DeleteAll       删除所有的键值对
